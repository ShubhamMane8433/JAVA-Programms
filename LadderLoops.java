class LadderLoops
{
	
	public static void main(String [] args)
	{
		int i=40;
		
		 if(i<10)
		 { 
	       System.out.println("Number is less than 10...!!!");
		 }
		 else if(i<20)
		 {
			 System.out.println("Number is less than 50 but greater than 10...!!!");
		 }
		else if(i<30)
		 {
			 System.out.println("Number is less than 50 but greater than 20...!!!");
		 }
		 else if(i<=40)
		 {
			 System.out.println("Number is less than 50 but greater than 30...!!!");
		 }
		 else
		 {
			 System.out.println(" Number is 50...!!!");
		 }
		 
	 }
	 
}
	